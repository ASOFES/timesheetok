class ScannerScreen extends StatefulWidget {
  const ScannerScreen({super.key});

  @override
  State<ScannerScreen> createState() => _ScannerScreenState();
}

class _ScannerScreenState extends State<ScannerScreen> {
  MobileScannerController cameraController = MobileScannerController();
  bool _isProcessing = false;
  bool _scannerActive = true;
  String? _lastScannedData;
  DateTime? _lastScanTime;
  User? _currentUser;
  
  // üìç √âTATS GPS ET CAM√âRA
  bool _isGpsActive = false;
  bool _isCameraActive = false;

  @override
  void initState() {
    super.initState();
    _loadUserData();
    _checkDeviceStates();
  }

  // üìç V√âRIFIER √âTATS GPS ET CAM√âRA
  Future<void> _checkDeviceStates() async {
    // V√©rifier l'√©tat de la cam√©ra
    try {
      final cameras = await cameraController.start();
      setState(() {
        _isCameraActive = true;
      });
      print('üì∑ Cam√©ra: ACTIVE');
    } catch (e) {
      setState(() {
        _isCameraActive = false;
      });
      print('üì∑ Cam√©ra: INACTIVE - $e');
    }

    // V√©rifier l'√©tat du GPS (permission)
    try {
      // Import Geolocator n√©cessaire - sera ajout√© plus tard
      setState(() {
        _isGpsActive = true; // Pour l'instant, supposons actif
      });
      print('üìç GPS: ACTIVE');
    } catch (e) {
      setState(() {
        _isGpsActive = false;
      });
      print('üìç GPS: INACTIVE - $e');
    }
  }

  @override
  void dispose() {
    cameraController.dispose();
    super.dispose();
  }

  Future<void> _loadUserData() async {
    try {
      final user = await ApiService.getUser();
      setState(() {
        _currentUser = user;
      });
    } catch (e) {
      print('Error loading user: $e');
    }
  }

  void _onQRCodeDetected(BarcodeCapture capture) async {
    // üîí PROTECTION ABSOLUE - VERROUS MULTIPLES
    
    // üõ°Ô∏è VERROU 1: Traitement en cours
    if (_isProcessing) {
      print('üö´ PROTECTION 1: Traitement en cours, scan BLOQU√â');
      return;
    }

    // üõ°Ô∏è VERROU 2: Scanner d√©sactiv√©
    if (!_scannerActive) {
      print('üö´ PROTECTION 2: Scanner d√©sactiv√©, scan BLOQU√â');
      return;
    }

    final List<Barcode> barcodes = capture.barcodes;
    if (barcodes.isEmpty) return;

    final String? qrData = barcodes.first.rawValue;
    if (qrData == null || qrData.isEmpty) {
      print('‚ö†Ô∏è QR vide ou invalide');
      return;
    }

    // üõ°Ô∏è VERROU 3: Anti-doublon temporel STRICT
    final now = DateTime.now();
    if (_lastScannedData == qrData && _lastScanTime != null) {
      final timeDiff = now.difference(_lastScanTime!).inMilliseconds;
      if (timeDiff < 3000) {  // 3 secondes minimum entre scans identiques
        print('üö´ PROTECTION 3: M√™me QR dans les ${timeDiff}ms, scan BLOQU√â');
        _showMessage('‚ö†Ô∏è SCAN TROP RAPIDE', 'Attendez 3 secondes entre les scans', Colors.orange);
        return;
      }
    }

    // üîí VERROUILLAGE TOTAL IMM√âDIAT - AUCUN AUTRE SCAN POSSIBLE
    setState(() {
      _isProcessing = true;
      _scannerActive = false;
      _lastScannedData = qrData;
      _lastScanTime = now;
    });

    print('üîí VERROUILLAGE ABSOLU: Scanner 100% d√©sactiv√©');
    print('üì± QR Code d√©tect√©: $qrData');
    
    // üîí ARR√äT FORC√â ET IMM√âDIAT DE LA CAM√âRA
    try {
      await cameraController.stop();
      setState(() {
        _isCameraActive = false;
      });
      print('üì∑ CAM√âRA ARR√äT√âE IMM√âDIATEMENT');
    } catch (e) {
      print('‚ö†Ô∏è Erreur arr√™t cam√©ra: $e');
    }

    // üéØ TRAITEMENT DU QR CODE
    await _processQRCode(qrData);
  }

  // üéØ FONCTION D'AFFICHAGE DE MESSAGES CLAIRS
  void _showMessage(String title, String message, Color color) {
    if (mounted) {
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(
          content: Column(
            mainAxisSize: MainAxisSize.min,
            crossAxisAlignment: CrossAxisAlignment.start,
            children: [
              Text(title, style: const TextStyle(fontWeight: FontWeight.bold, fontSize: 16)),
              Text(message, style: const TextStyle(fontSize: 14)),
            ],
          ),
          backgroundColor: color,
          duration: const Duration(seconds: 4),
          behavior: SnackBarBehavior.floating,
        ),
      );
    }
  }

  Future<void> _processQRCode(String qrData) async {
    try {
      // üîç V√âRIFICATION UTILISATEUR
      if (_currentUser == null) {
        print('‚ùå ERREUR: Utilisateur non connect√©');
        _showMessage('‚ùå ERREUR CONNEXION', 'Utilisateur non connect√©. Reconnectez-vous.', Colors.red);
        _showErrorDialog('‚ùå Erreur de connexion', 'Utilisateur non connect√©');
        return;
      }

      print('üë§ Utilisateur connect√©: ${_currentUser!.displayName} (ID: ${_currentUser!.id})');
      
      // üîç AFFICHAGE MESSAGE DE TRAITEMENT
      _showMessage('üîç ANALYSE EN COURS', 'V√©rification du QR code...', Colors.blue);

      // üîí V√âRIFICATION ANTI-DOUBLON QUOTIDIEN
      print('üîç V√©rification anti-doublon quotidien pour QR: $qrData');
      final alreadyUsed = await ApiService.checkQRUsedToday(qrData, _currentUser!.id);
      if (alreadyUsed) {
        print('üö´ DOUBLON QUOTIDIEN D√âTECT√â: QR d√©j√† utilis√© aujourd\'hui');
        _showMessage('üö´ DOUBLON D√âTECT√â', 'Vous avez d√©j√† point√© avec ce QR code aujourd\'hui !', Colors.orange);
        _showErrorDialog('üö´ Pointage en double', 'Vous avez d√©j√† utilis√© ce QR code aujourd\'hui.\n\nUn seul pointage par QR code par jour est autoris√©.');
        return;
      }

      print('‚úÖ V√©rification anti-doublon: QR autoris√©');

      // üéØ FONCTION POUR D√âTERMINER LE TYPE DE SERVICE
      String _getServiceType(int timesheetTypeId, Map<String, dynamic> qrJson) {
        // V√©rifier d'abord si le QR contient une indication explicite
        if (qrJson.containsKey('serviceType')) {
          return qrJson['serviceType'].toString();
        }
        if (qrJson.containsKey('type')) {
          return qrJson['type'].toString();
        }
        
        // Sinon, d√©terminer selon le timesheetTypeId
        switch (timesheetTypeId) {
          case 1:
            return 'D√©but de Service';
          case 2:
            return 'Fin de Service';
          case 3:
            return 'Pause D√©but';
          case 4:
            return 'Pause Fin';
          case 5:
            return 'Pause D√©jeuner';
          default:
            return 'Service Standard';
        }
      }

      // PARSING QR - LOGIQUE COPI√âE DE VOTRE APK QUI MARCHE
      print('QR Code re√ßu: $qrData');
      print('Longueur QR: ${qrData.length} caract√®res');
      
      // üîç PARSING DU QR CODE JSON
      _showMessage('üîç VALIDATION', 'Analyse du format QR code...', Colors.blue);
      
      Map<String, dynamic> qrJson;
      try {
        qrJson = jsonDecode(qrData);
        print('‚úÖ QR JSON pars√© avec succ√®s: $qrJson');
        print('üìã Cl√©s disponibles: ${qrJson.keys.toList()}');
        _showMessage('‚úÖ FORMAT VALIDE', 'QR code reconnu et valid√©', Colors.green);
      } catch (e) {
        print('‚ùå ERREUR PARSING JSON: $e');
        _showMessage('‚ùå FORMAT INVALIDE', 'QR code non reconnu ou corrompu', Colors.red);
        _showErrorDialog('‚ùå QR Code Invalide', 'Le QR code scann√© n\'est pas au bon format.\n\nFormat attendu: JSON valide\n\nErreur: ${e.toString()}');
        return;
      }

      // Extraire les donn√©es selon le format - COMME VOTRE APK
      int siteId, planningId, timesheetTypeId;
      String siteName = '';
      int? employeeId;
      
      // Format Vercel exact (userId + userName + planningId + timeSheetId) - VOTRE FORMAT
      if (qrJson.containsKey('userId') && qrJson.containsKey('userName') && qrJson.containsKey('planningId')) {
        siteId = 1; // Site par d√©faut
        planningId = qrJson['planningId'];
        timesheetTypeId = qrJson['timeSheetTypeId'] ?? 1;
        siteName = 'test'; // Site fixe pour correspondre au QR
        employeeId = qrJson['userId']; // ID de l'utilisateur sp√©cifique
        print('Format d√©tect√©: Vercel (exact)');
        print('  userId: ${qrJson['userId']}');
        print('  userName: ${qrJson['userName']}');
        print('  planningId: ${qrJson['planningId']}');
        print('  timeSheetTypeId: ${qrJson['timeSheetTypeId']}');
        print('  siteName: $siteName');
      }
      // Format Vercel (site + employ√©) - Format complet
      else if (qrJson.containsKey('siteId') && qrJson.containsKey('planningId') && qrJson.containsKey('timesheetTypeId')) {
        siteId = qrJson['siteId'];
        planningId = qrJson['planningId'];
        timesheetTypeId = qrJson['timesheetTypeId'];
        siteName = qrJson['siteName'] ?? 'Site inconnu';
        employeeId = qrJson['employeeId']; // ID de l'employ√© sp√©cifique
        print('Format d√©tect√©: Vercel (complet)');
      }
      // Format Vercel (site + employ√©) - Format sans employeeId
      else if (qrJson.containsKey('siteId') && qrJson.containsKey('planningId')) {
        siteId = qrJson['siteId'];
        planningId = qrJson['planningId'];
        timesheetTypeId = qrJson['timesheetTypeId'] ?? 1;
        siteName = qrJson['siteName'] ?? 'Site inconnu';
        print('Format d√©tect√©: Vercel (sans employeeId)');
      }
      // Format raccourci (notre app)
      else if (qrJson.containsKey('uid') && qrJson.containsKey('pid')) {
        siteId = 1; // Site par d√©faut
        planningId = qrJson['pid'];
        timesheetTypeId = 1; // Type par d√©faut
        siteName = 'Site par d√©faut';
        employeeId = qrJson['uid']; // ID de l'utilisateur sp√©cifique
        print('Format d√©tect√©: raccourci');
        print('  uid: ${qrJson['uid']}');
      }
      // Format inconnu - Essayons d'extraire intelligemment
      else {
        print('‚ùå Format non reconnu, tentative d\'extraction intelligente');
        print('QR JSON re√ßu: $qrJson');
        print('Cl√©s disponibles: ${qrJson.keys.toList()}');
        
        // üîç EXTRACTION INTELLIGENTE DU PLANNING ID
        siteId = 1;
        planningId = 5; // Valeur par d√©faut
        timesheetTypeId = 1;
        siteName = 'Site par d√©faut';
        
        // Essayer d'extraire le planningId sous toutes les formes possibles
        if (qrJson.containsKey('planningId')) {
          planningId = qrJson['planningId'];
          print('‚úÖ planningId trouv√©: $planningId');
        } else if (qrJson.containsKey('pid')) {
          planningId = qrJson['pid'];
          print('‚úÖ pid trouv√©: $planningId');
        } else if (qrJson.containsKey('planning_id')) {
          planningId = qrJson['planning_id'];
          print('‚úÖ planning_id trouv√©: $planningId');
        } else if (qrJson.containsKey('id')) {
          planningId = qrJson['id'];
          print('‚úÖ id utilis√© comme planningId: $planningId');
        } else {
          // Essayer de trouver tout nombre dans le QR
          for (String key in qrJson.keys) {
            final value = qrJson[key];
            if (value is int && value > 0 && value < 1000) {
              planningId = value;
              print('‚úÖ Nombre trouv√© ($key): $planningId');
              break;
            }
          }
          
          if (planningId == 5) { // Si on n'a toujours rien trouv√©
            planningId = DateTime.now().millisecondsSinceEpoch % 1000; // ID bas√© sur timestamp
            print('‚ö†Ô∏è Aucun ID trouv√©, g√©n√©ration automatique: $planningId');
          }
        }
        
        // Essayer d'extraire le timesheetTypeId aussi
        if (qrJson.containsKey('timeSheetTypeId')) {
          timesheetTypeId = qrJson['timeSheetTypeId'];
        } else if (qrJson.containsKey('timesheetTypeId')) {
          timesheetTypeId = qrJson['timesheetTypeId'];
        } else if (qrJson.containsKey('type')) {
          timesheetTypeId = qrJson['type'];
        }
        
        // Essayer d'extraire l'employeeId aussi pour la s√©curit√©
        if (qrJson.containsKey('userId')) {
          employeeId = qrJson['userId'];
          print('‚úÖ userId trouv√©: $employeeId');
        } else if (qrJson.containsKey('employeeId')) {
          employeeId = qrJson['employeeId'];
          print('‚úÖ employeeId trouv√©: $employeeId');
        } else if (qrJson.containsKey('uid')) {
          employeeId = qrJson['uid'];
          print('‚úÖ uid trouv√©: $employeeId');
        }
        
        print('üîß Extraction intelligente termin√©e: Planning=$planningId, Type=$timesheetTypeId, Employee=$employeeId');
      }

      // üîí V√âRIFICATION S√âCURIT√â: L'utilisateur peut-il scanner CE QR ?
      if (employeeId != null) {
        // Si le QR contient un employeeId/userId, v√©rifier qu'il correspond √† l'utilisateur connect√©
        if (employeeId != _currentUser!.id) {
          print('‚ùå S√âCURIT√â: Utilisateur connect√© (${_currentUser!.id}) ‚â† QR employeeId ($employeeId)');
          _showMessage('‚ùå QR NON AUTORIS√â', 'Ce QR code appartient √† un autre utilisateur', Colors.red);
          _showErrorDialog(
            'üö´ QR Code Non Autoris√©', 
            'Ce QR code a √©t√© g√©n√©r√© pour un autre employ√©.\n\n'
            'üë§ Utilisateur connect√©: ${_currentUser!.displayName} (ID: ${_currentUser!.id})\n'
            'üîí QR code pour: ID $employeeId\n\n'
            'Vous ne pouvez scanner que vos propres QR codes.'
          );
          return;
        } else {
          print('‚úÖ S√âCURIT√â: QR code autoris√© pour l\'utilisateur ${_currentUser!.id}');
          _showMessage('‚úÖ QR AUTORIS√â', 'QR code v√©rifi√© pour ${_currentUser!.displayName}', Colors.green);
        }
      } else {
        // Si pas d'employeeId dans le QR, c'est un QR g√©n√©rique (autoris√©)
        print('‚ÑπÔ∏è S√âCURIT√â: QR g√©n√©rique (pas d\'employeeId) - autoris√©');
        _showMessage('‚ÑπÔ∏è QR G√âN√âRIQUE', 'QR code sans restriction d\'utilisateur', Colors.orange);
      }

      // üéØ D√âTERMINER LE TYPE DE SERVICE
      String serviceType = _getServiceType(timesheetTypeId, qrJson);
      
      print('üéØ Donn√©es extraites: Site $siteId, Planning $planningId, Type $timesheetTypeId');
      print('üîß Type de service d√©termin√©: $serviceType');
      _showMessage('üìä DONN√âES EXTRAITES', 'Site: $siteName\nPlanning: $planningId\nType: $serviceType', Colors.blue);

      // üöÄ CR√âATION DU POINTAGE
      _showMessage('üöÄ ENREGISTREMENT', 'Envoi du pointage au serveur...', Colors.blue);
      
      final result = await ApiService.createTimesheet(
        siteId: siteId,
        planningId: planningId,
        timesheetTypeId: timesheetTypeId,
        qrData: qrData,
      );

      if (result['success'] == true && mounted) {
        print('üéâ SUCC√àS: Pointage enregistr√© avec succ√®s');
        
        // üéâ MESSAGES DE SUCC√àS MULTIPLES D√âTAILL√âS
        _showMessage('üéâ POINTAGE R√âUSSI !', 'Enregistr√©: $serviceType - $siteName', Colors.green);
        
        final now = DateTime.now();
        final timeString = '${now.day.toString().padLeft(2, '0')}/${now.month.toString().padLeft(2, '0')}/${now.year} ${now.hour.toString().padLeft(2, '0')}:${now.minute.toString().padLeft(2, '0')}:${now.second.toString().padLeft(2, '0')}';
        
        _showSuccessDialog(
          'üéâ POINTAGE R√âUSSI !', 
          'Votre pointage a √©t√© enregistr√© avec succ√®s !\n\n'
          'üîß Type: $serviceType\n'
          'üìç Site: $siteName\n'
          'üìã Planning: $planningId\n'
          'üë§ Utilisateur: ${_currentUser!.displayName}\n'
          'üìß Email: ${_currentUser!.email}\n'
          '‚è∞ Date/Heure: $timeString'
        );
      } else {
        print('‚ùå √âCHEC: Pointage non enregistr√©');
        _showMessage('‚ùå √âCHEC POINTAGE', 'Impossible d\'enregistrer le pointage', Colors.red);
        _showErrorDialog('‚ùå √âchec du pointage', 'Le pointage n\'a pas pu √™tre enregistr√©.\n\nV√©rifiez votre connexion internet.');
      }
    } catch (e) {
      print('‚ùå EXCEPTION: $e');
      _showMessage('‚ùå ERREUR TECHNIQUE', 'Probl√®me lors du traitement: ${e.toString()}', Colors.red);
      if (mounted) {
        _showErrorDialog('‚ùå Erreur technique', 'Une erreur technique est survenue:\n\n${e.toString()}\n\nVeuillez r√©essayer.');
      }
    }
  }

